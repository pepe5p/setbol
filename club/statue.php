                        <tr>
                            <td>§ 17</td>
                            <td>
                                Władzami Klubu są:
                                <div class="space"></div>
                                -Walne Zebranie Klubu
                                -Zarząd
                                -Komisja Rewizyjna
                                -Kadencja władz trwa 4 lata.
                                -Wybór władz następuje w głosowaniu tajnym.
                                -Uchwały wszystkich władz Klubu zapadają zwykłą większo­ścią głosów.
                            </td>
                        </tr>
                        <tr>
                            <td>§ 18</td>
                            <td>
                                Walne Zebranie Klubu jest najwyższą władzą Klubu i może być zwyczajne lub nadzwyczajne.
                                Walne Zebranie Sprawozdawczo – Wyborcze Klubu zwoły­wane jest przez Zarząd, co 4 lata lub wcześniej, gdy zaistnieje sytuacja grożąca rozwiązaniem Klubu.
                                Walne Zebranie Sprawozdawcze zwoływane jest przez Za­rząd raz na rok.
                                Nadzwyczajne Walne Zebranie Klubu zwoływane jest przez Zarząd Klubu:
                                <div class="space"></div>
                                -z własnej inicjatywy,<br>
                                -na wniosek Komisji Rewizyjnej,<br>
                                -na wniosek 50% ogółu członków.<br>
                                <div class="space"></div>
                                Nadzwyczajne Walne Zebranie Klubu odbywa się nie póź­niej niż w ciągu miesiąca od złożenia wniosku lub podjęcia uchwały i obraduje nad sprawami, dla których zostało zwołane.
                            </td>
                        </tr>
                        <tr>
                            <td>§ 19</td>
                            <td>
                                Do wyłącznej kompetencji Walnego Zebrania Klubu należy:
                                <div class="space"></div>
                                -uchwalanie programów sportowej, organizacyjnej i finan­sowej działalności Klubu,<br>
                                -rozpatrywanie i przyjmowanie sprawozdań Zarządu Klubu i Ko­misji Rewizyjnej,<br>
                                -uchwalanie na wniosek Komisji Rewizyjnej absolutorium dla Zarządu Klubu, wybór Zarządu i Komisji Rewizyjnej,<br>
                                -ustalenie wysokości i trybu płacenia składek członkow­skich,<br>
                                -rozpatrywanie i rozstrzyganie innych spraw wniesionych pod obrady Walnego Zebrania Klubu.<br>
                                -Walne Zebranie Klubu jest prawomocne w pierwszym termi­nie przy obecności, co najmniej 50% członków upraw­nionych do głosowania,
                                a w drugim terminie bez względu na liczbę obecnych, o ile termin ten był podany w zawiado­mieniu.<br>
                                -W Walnym Zebraniu Klubu mogą brać udział z głosem do­radczym osoby zaproszone przez Zarząd.
                            </td>
                        </tr>
                        <tr>
                            <td>§ 20</td>
                            <td>
                                Zarząd Klubu składa się z 3-7 członków wy­bieranych przez Walne Zebranie Klubu, którzy spośród siebie wybierają Prezesa, V-ce Prezesa, Skarbnika, Sekretarza lub Członków Zarządu.
                                Zebrania Zarządu Klubu odbywają się w miarę potrzeby, nie rzadziej jednak niż jeden raz na 12 miesięcy.
                                Do ważności uchwał Zarządu Klubu konieczna jest obec­ność, co najmniej 1/2 liczby członków Za­rządu, w tym Prezesa lub V-ce Prezesa.
                                W okresie pomiędzy posiedzeniami Zarządu jego uprawnie­nia przysługują Prezesowi.
                            </td>
                        </tr>
                        <tr>
                            <td>§ 21</td>
                            <td>
                                Do zadań Zarządu Klubu należy kierowanie bieżąca działal­nością Klubu w okresach pomiędzy Walnymi Zebraniami Klu­bu, a w szczególności:
                                <div class="space"></div>
                                -reprezentowanie Klubu na zewnątrz i działanie w jego imie­niu,<br>
                                -wykonywanie uchwał Walnego Zebrania Klubu,<br>
                                -uchwalanie okresowych planów działania i preliminarzy bu­dżetowych,<br>
                                -powoływanie i rozwiązywanie komisji problemowych,<br>
                                -przyjmowanie i skreślanie członków,<br>
                                -zarządzanie majątkiem i funduszami Klubu zgodnie z obo­wiązującymi przepisami,<br>
                                -podejmowanie decyzji w sprawie zmian barw klubowych, podpisywanie umów z tym związanych<br>
                                -podejmowanie działań zmierzających do pozyskania sponsorów, zawieranie umów związanych ze sponsoringiem<br>
                                -składanie sprawozdań z działalności Klubu.
                            </td>
                        </tr>
                        <tr>
                            <td>§ 22</td>
                            <td>
                                Członek Zarządu Klubu może być zawieszony w czynności lub odwołany ze składu Zarządu, jeżeli nie wykonuje przyję­tych obowiązków, działa niezgodnie ze statutem bądź w inny sposób zawiódł zaufanie członków Klubu.
                                Uchwałę o zawieszeniu w czynnościach lub odwołaniu człon­ka Zarządu podejmuje Zarząd zwykłą większością głosów przy obecności, co najmniej 50% uprawnionych do głosowania.
                                Usunięty lub zawieszony członek Zarządu ma prawo odwo­łania się do Walnego Zebrania Klubu w ciągu 30 dni od podjęcia uchwały o usunięciu lub zawieszeniu.
                            </td>
                        </tr>
                        <tr>
                            <td>§ 23</td>
                            <td>
                                Komisja Rewizyjna jest organem kontrolującym całokształt działalności Klubu.
                                Komisja Rewizyjna składa się z 3 członków wybieranych przez Walne Zebranie Klubu.
                                Komisja wybiera ze swego grona Przewodniczącego i Sekre­tarza.
                                Komisja Rewizyjna działa w oparciu o regulamin zatwierdzo­ny przez Walne Zebranie Klubu.
                                Do czynności Komisji Rewizyjnej należy:
                                przeprowadzanie okresowych kontroli działalności statu­towej i finansowej Klubu,
                                wydawanie zaleceń pokontrolnych w przypadku stwier­dzenia uchybień w działalności, określenie terminów oraz sposobów ich usunięcia,
                                składanie sprawozdań z całokształtu swej działalności na Walnym Zebraniu Klubu oraz stawianie wniosku o udzie­lenie lub odmowę udzielenia absolutorium ustępującemu Zarządowi,
                                składanie zastrzeżeń w stosunku do projektowanych uchwał, postanowień i przedsięwzięć Zarządu, jeżeli Ko­misja dojdzie do wniosku, że spowodują one lub mogą spowodować nieprawidłowości w działaniu Klubu,
                                występowanie z żądaniem zwołania Nadzwyczajnego Wal­nego Zebrania Klubu, względnie z wnioskiem o zwołanie posiedzenia Zarządu w przypadkach stwierdzenia niezgod­nych z prawem lub Statutem bądź istotnymi interesami Klubu działań Zarządu.
                            </td>
                        </tr>
                        <tr>
                            <td>§ 24</td>
                            <td>
                                Członek Komisji Rewizyjnej może być zawieszony w czyn­nościach lub odwołany z jej składu, jeżeli nie wykonuje przy­jętych obowiązków, działa niezgodnie ze Statutem, bądź w inny sposób zawiódł zaufanie członków Klubu.
                                Uchwałę o zawieszeniu w czynnościach lub odwołaniu człon­ka Komisji Rewizyjnej podejmuje Zarząd Klubu więk­szością 2/3 głosów przy obecności, co najmniej połowy uprawnionych do głosowania.
                                Odwołany lub zawieszony członek Komisji Rewizyjnej ma prawo odwołać się do Walnego Zebrania Klubu w terminie 30 dni od podjęcia uchwały o odwołaniu lub zawie­szeniu.
                            </td>
                        </tr>
                        <tr>
                            <td>§ 25</td>
                            <td>
                                W przypadku powstania wakatu w Zarządzie lub Komisji Re­wizyjnej – władzom tym przysługuje prawo dokooptowania no­wych członków spośród członków Klubu. 
                                Liczba osób doko­optowanych nie może przekroczyć 1/3 pochodzących z wyboru przez Walne Zebranie Członków.
                            </td>
                        </tr>
